<h5><strong>Adoptar es un acto de responsabilidad y compromiso por lo que
es importante que estes capacitado para hacerlo. Un perro puede vivir
entre 14 y 18 años y durante todo ese tiempo dependerá de vos.</strong></h5>

<br>
<h5 style="color:#D35400;"> <p>Algunas cuestiones para tener en cuenta son el espacio disponible de tu casa,
el tiempo que tengas para brindarle compañía, actividad física y cuidado.
También los costos asociados a su alimentación, atención veterinaria y estadía durante tus vacaciones.</p>

<p>Para asegurarte una convivencia feliz tu elección debe estar orientada al estilo de vida que llevas
y el tiempo real que le podes dedicar.</p></h5>

<br>
<hr>
<br>
<p><b>Aquí te damos algunos tips, pero recordá que lo más importante es adoptar con conciencia y
con el corazón.</b></p>
<p><span style="color: #D35400"><strong>Un cachorro:</strong> (45 días-12 meses) </span>
es precioso pero, requiere de mucho tiempo y esfuerzo. Come 4 veces al día, puede llorar por las noches
y en las ausencias de los dueños. Mastica objetos, hace pis y caca en cualquier lado, requiere educación
y paciencia, es un bebe durante un año. No hay garantías con respecto al tamaño definitivo.
En caso de haber niños chicos en el hogar, hay que marcar la diferencia entre mascota y juguete.
Sin embargo es muy satisfactorio ir viendo el crecimiento y desarrollo de un cachorrito hasta
convertirse en adulto.</p>
<br>
<p><span style="color: #D35400"><strong>Un joven:</strong> (1 año-5 años) </span>
es juguetón pero más adulto. Son más hábiles para centrarse en la educación. Generalmente ya está
con la vacunación completa, lo que no hay riesgo de contraer alguna enfermedad. Están esterilizados.
Ya se puede sacarlo a pasear y bañarlo. Come dos veces al día. Ya tiene el tamaño definitivo, no
cambiara de aspecto.</p>
<br>
<p><span style="color: #D35400"><strong>Un adulto:</strong> (5 años-9años) </span>
no genera problemas por quedarse algunas horas solo. Es probable que duerma toda lo noche.
Se habitúa fácilmente a su nueva familia. Aceptan su sitio dentro de la jerarquía del hogar,
se adaptan a órdenes básicas, si es que no las tienen adquiridas de antes. Se muestran gratificante
y toman mucho cariño a su nueva familia y casa.</p>
<br>
<p><span style="color: #D35400"><strong>Un abuelo:</strong> (10 años-día de su muerte) </span>
nos dejan muchísimo tiempo para nosotros. No requieren tanto trabajo. Son muy calmos y sedentarios.
Aunque el tiempo de compañía es seguramente menor, también se merecen un hogar. Podemos darle durante
los años que le quedan una vida digna y agradable. Estarán súper agradecidos, esperaron su hogar mucho
tiempo.</p>
